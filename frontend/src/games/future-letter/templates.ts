function getSeason(monthIndex: number): Season {
  if (monthIndex === 11 || monthIndex <= 1) return 'winter';
  if (monthIndex >= 2 && monthIndex <= 4) return 'spring';
  if (monthIndex >= 5 && monthIndex <= 7) return 'summer';
  return 'autumn';
}

function pickVariant<T>(variants: T[], seed: number): T {
  return variants[((seed % variants.length) + variants.length) % variants.length];
}

function getTemperatureTail(temp: number): string {
  if (temp <= 40) {
    return 'Сценарий будущего остается реалистичным и светлым: устойчивость строится на внимании к людям и точности решений.';
  }
  if (temp <= 70) {
    return 'Будущее выглядит чуть смелее привычного, но в нем по-прежнему много надежности, поддержки и уважения.';
  }
  return 'Даже в необычно суровом, постапокалиптическом контуре люди сохраняют доброту, а команды учатся действовать с надеждой.';
}

function formatDateRu(date: Date): string {
  const dd = String(date.getDate()).padStart(2, '0');
  const mm = String(date.getMonth() + 1).padStart(2, '0');
  const yyyy = date.getFullYear();
  return `${dd}.${mm}.${yyyy}`;
}

function formatMonthRu(date: Date): string {
  return `${MONTHS_RU[date.getMonth()]} ${date.getFullYear()}`;
}

type BuildLetterParams = {
  horizon: Horizon;
  name: string;
  temperature: number;
  monthDate: Date;
};

export function getTemperatureToneHint(temp: number): string {
  if (temp <= 40) return 'Реалистично и тепло';
  if (temp <= 70) return 'Оптимистично и фантастично';
  return 'Постапокалипсис с надеждой';
}

export function buildLetterTemplate({
  horizon,
  name,
  temperature,
  monthDate,
}: BuildLetterParams): LetterData {
  const safeName = name.trim() || 'Гостья';
  const now = new Date();
  const dateFrom =
    horizon === '5y'
      ? new Date(now.getFullYear() + 5, now.getMonth(), now.getDate())
      : horizon === '50y'
        ? new Date(now.getFullYear() + 50, now.getMonth(), now.getDate())
        : monthDate;

  const season = getSeason(dateFrom.getMonth());
  const seed = dateFrom.getMonth() + temperature;

  const weatherBlock = pickVariant(WEATHER_BY_SEASON[season], seed);
  const seasonalWork = pickVariant(WORK_BY_SEASON[season], seed + 1);
  const wishBlock = pickVariant(WISH_BY_SEASON[season], seed + 2);
  const closingLine = pickVariant(CLOSING_LINES, seed + 3);

  const monthNuance = MONTH_WORK_NUANCE[monthDate.getMonth()];
  const workBlock = horizon === 'month' ? `${monthNuance} ${seasonalWork}` : seasonalWork;
  const originPrefix =
    horizon === '50y'
      ? 'Пишу тебе из далекого будущего, где технологии стали частью каждого решения, но человеческое участие по-прежнему главное.'
      : horizon === '5y'
        ? 'Пишу тебе из будущего на несколько лет вперед, где скорость изменений выросла, но ценность внимательных решений только усилилась.'
        : `Пишу тебе из ${formatMonthRu(monthDate)} - из времени, где ежедневная точность и доброе отношение к людям дают большой результат.`;

  return {
    title:
      horizon === '50y'
        ? 'Письмо от кибернетического организма Председателя Правления одного синего банка'
        : 'Письмо от Председателя Правления одного синего банка',
    greeting: `Дорогая ${safeName}!`,
    authorLine:
      horizon === '50y'
        ? 'От кибернетического организма Председателя Правления одного синего банка'
        : 'От Председателя Правления одного синего банка',
    dateLine: formatDateRu(dateFrom),
    originBlock: `${originPrefix} ${getTemperatureTail(temperature)}`,
    weatherBlock,
    workBlock,
    wishBlock,
    closingLine,
    signature:
      horizon === '50y'
        ? 'С уважением и теплом, кибернетический организм Председателя Правления одного синего банка'
        : 'С уважением и теплом, Председатель Правления одного синего банка',
  };
}
